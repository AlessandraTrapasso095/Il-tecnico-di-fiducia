-- Directory + categories + saved professionals + messaging improvements
-- Notes:
-- - No fake users/professionals/reviews are seeded.
-- - Categories are a static taxonomy; safe to seed separately if desired.

-- Directory (public fields only; no email/phone)
create table if not exists public.professional_directory (
  id uuid primary key references public.professional_profiles(id) on delete cascade,
  first_name text not null default '',
  last_name text not null default '',
  province_code text references public.provinces(code),
  headline text,
  bio text,
  specializations text[] not null default '{}',
  avatar_url text,
  cover_url text,
  available_remote boolean not null default false,
  available_travel boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.professional_directory enable row level security;
alter table public.professional_directory force row level security;

create table if not exists public.customer_directory (
  id uuid primary key references public.profiles(id) on delete cascade,
  first_name text not null default '',
  last_name text not null default '',
  province_code text references public.provinces(code),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

alter table public.customer_directory enable row level security;
alter table public.customer_directory force row level security;

-- Professional availability flags used by search filters
alter table public.professional_profiles
  add column if not exists available_remote boolean not null default false,
  add column if not exists available_travel boolean not null default false;

-- Conversation preview helpers
alter table public.conversations
  add column if not exists last_message_body text,
  add column if not exists last_message_sender_id uuid references public.profiles(id) on delete set null;

-- Saved professionals (customer bookmarks)
create table if not exists public.saved_professionals (
  customer_id uuid not null references public.profiles(id) on delete cascade,
  professional_id uuid not null references public.professional_profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (customer_id, professional_id)
);

alter table public.saved_professionals enable row level security;
alter table public.saved_professionals force row level security;

-- Categories (taxonomy)
create table if not exists public.categories (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text not null unique,
  image_url text,
  created_at timestamptz not null default now()
);

alter table public.categories enable row level security;
alter table public.categories force row level security;

create table if not exists public.professional_categories (
  professional_id uuid not null references public.professional_profiles(id) on delete cascade,
  category_id bigint not null references public.categories(id) on delete restrict,
  created_at timestamptz not null default now(),
  primary key (professional_id, category_id)
);

alter table public.professional_categories enable row level security;
alter table public.professional_categories force row level security;

-- Helpers: can a professional see a customer directory row?
create or replace function public.professional_can_view_customer_dir(customer_uuid uuid)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.conversations c
    where c.professional_id = auth.uid()
      and c.customer_id = customer_uuid
  ) or (select public.is_admin());
$$;

-- Keep directory tables in sync (public fields only)
create or replace function public.tg_profiles_sync_directories()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  -- NOTE: professional_directory references professional_profiles, which may not exist
  -- yet when a profile row is inserted (signup trigger creates profiles first).
  -- professional_directory is synced from professional_profiles triggers instead.
  if new.role = 'customer' then
    insert into public.customer_directory (id, first_name, last_name, province_code)
    values (new.id, new.first_name, new.last_name, new.province_code)
    on conflict (id) do update
      set first_name = excluded.first_name,
          last_name = excluded.last_name,
          province_code = excluded.province_code,
          updated_at = now();
  elsif new.role = 'professional' then
    update public.professional_directory
    set first_name = new.first_name,
        last_name = new.last_name,
        province_code = new.province_code,
        updated_at = now()
    where id = new.id;
  end if;

  return new;
end;
$$;

drop trigger if exists profiles_sync_directories on public.profiles;
create trigger profiles_sync_directories
after insert or update of first_name, last_name, province_code, role
on public.profiles
for each row execute function public.tg_profiles_sync_directories();

create or replace function public.tg_professional_profiles_sync_directory()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into public.professional_directory (
    id,
    headline,
    bio,
    specializations,
    avatar_url,
    cover_url,
    available_remote,
    available_travel
  )
  values (
    new.id,
    new.headline,
    new.bio,
    new.specializations,
    new.avatar_url,
    new.cover_url,
    coalesce(new.available_remote, false),
    coalesce(new.available_travel, false)
  )
  on conflict (id) do update
    set headline = excluded.headline,
        bio = excluded.bio,
        specializations = excluded.specializations,
        avatar_url = excluded.avatar_url,
        cover_url = excluded.cover_url,
        available_remote = excluded.available_remote,
        available_travel = excluded.available_travel,
        updated_at = now();

  return new;
end;
$$;

drop trigger if exists professional_profiles_sync_directory on public.professional_profiles;
create trigger professional_profiles_sync_directory
after insert or update of headline, bio, specializations, avatar_url, cover_url, available_remote, available_travel
on public.professional_profiles
for each row execute function public.tg_professional_profiles_sync_directory();

-- Ensure new professionals have directory rows even if the order of triggers changes.
create or replace function public.tg_ensure_professional_directory_row()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  prof public.profiles%rowtype;
begin
  select *
  into prof
  from public.profiles p
  where p.id = new.id;

  if prof.role = 'professional' then
    insert into public.professional_directory (id, first_name, last_name, province_code)
    values (new.id, prof.first_name, prof.last_name, prof.province_code)
    on conflict (id) do nothing;
  end if;

  return new;
end;
$$;

drop trigger if exists ensure_professional_directory_row on public.professional_profiles;
create trigger ensure_professional_directory_row
after insert on public.professional_profiles
for each row execute function public.tg_ensure_professional_directory_row();

