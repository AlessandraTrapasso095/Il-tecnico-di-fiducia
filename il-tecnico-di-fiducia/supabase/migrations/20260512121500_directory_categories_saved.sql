-- Contact request trigger: create conversation AND initial message (so chat is real from day 1)
create or replace function public.tg_contact_request_create_conversation()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  conv_id uuid;
begin
  insert into public.conversations (request_id, customer_id, professional_id, status)
  values (new.id, new.customer_id, new.professional_id, new.status)
  on conflict (request_id) do update
    set status = excluded.status
  returning id into conv_id;

  insert into public.messages (conversation_id, sender_id, body)
  values (conv_id, new.customer_id, new.message);

  return new;
end;
$$;

-- Message trigger: keep conversation preview fields in sync
create or replace function public.tg_message_set_last_message_at()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  update public.conversations
  set last_message_at = new.created_at,
      last_message_body = new.body,
      last_message_sender_id = new.sender_id
  where id = new.conversation_id;

  return new;
end;
$$;

-- RLS POLICIES

-- Directory: professionals can browse all; customers only active subscribers
drop policy if exists "Professional directory: professionals read" on public.professional_directory;
create policy "Professional directory: professionals read"
on public.professional_directory
for select
to authenticated
using ((select public.is_professional()) or (select public.is_admin()));

drop policy if exists "Professional directory: customers read active only" on public.professional_directory;
create policy "Professional directory: customers read active only"
on public.professional_directory
for select
to authenticated
using (
  (select public.is_customer())
  and (select public.professional_is_active_subscriber(public.professional_directory.id))
);

drop policy if exists "Professional directory: owner update" on public.professional_directory;
create policy "Professional directory: owner update"
on public.professional_directory
for update
to authenticated
using (id = (select auth.uid()))
with check (id = (select auth.uid()));

drop policy if exists "Professional directory: admin update" on public.professional_directory;
create policy "Professional directory: admin update"
on public.professional_directory
for update
to authenticated
using ((select public.is_admin()))
with check ((select public.is_admin()));

-- Customers: visible to themselves; and to professionals they have a conversation with
drop policy if exists "Customer directory: self read" on public.customer_directory;
